import type { ColumnDef } from "@tanstack/react-table"
import { Check, Copy } from "lucide-react"

import type { EmpPublic } from "@/client"
import { Button } from "@/components/ui/button"
import { useCopyToClipboard } from "@/hooks/useCopyToClipboard"
import { cn } from "@/lib/utils"
import { EmpActionsMenu } from "./EmpActionsMenu"

function CopyId({ id }: { id: string }) {
  const [copiedText, copy] = useCopyToClipboard()
  const isCopied = copiedText === id

  return (
    <div className="flex items-center gap-1.5 group">
      <span className="font-mono text-xs text-muted-foreground">{id}</span>
      <Button
        variant="ghost"
        size="icon"
        className="size-6 opacity-0 group-hover:opacity-100 transition-opacity"
        onClick={() => copy(id)}
      >
        {isCopied ? (
          <Check className="size-3 text-green-500" />
        ) : (
          <Copy className="size-3" />
        )}
        <span className="sr-only">Copy ID</span>
      </Button>
    </div>
  )
}

export const columns: ColumnDef<EmpPublic>[] = [
  {
    accessorKey: "empcode",
    header: "Emp Code",
    cell: ({ row }) => <CopyId id={row.original.empcode} />,
  },
  {
    accessorKey: "name",
    header: "Name",
    cell: ({ row }) => (
      <span className="font-medium">{row.original.name}</span>
    ),
  },
  {
    accessorKey: "ownerdep.dep_name",
    header: "Department",
    cell: ({ row }) => {
      const depName = row.original.ownerdep?.dep_name
      return (
        <span
          className={cn(
            "text-muted-foreground",
            !depName && "italic"
          )}
        >
          {depName}
        </span>
      )
    },
  },
  {
    accessorKey: "workemail",
    header: "Work Email",
  },
  {
    accessorKey: "mobile_number",
    header: "Mobile",
  },
  {
    accessorKey: "address",
    header: "Address",
    cell: ({ row }) => {
      const address = row.original.address
      return (
        <span
          className={cn(
            "max-w-xs truncate block text-muted-foreground",
            !address && "italic"
          )}
        >
          {address || "No address"}
        </span>
      )
    },
  },
  {
    id: "actions",
    header: () => <span className="sr-only">Actions</span>,
    cell: ({ row }) => (
      <div className="flex justify-end">
        <EmpActionsMenu emp={row.original} />
      </div>
    ),
  },
]

